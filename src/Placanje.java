
public class Placanje{
private int mjesec;
private boolean platio;


public int getMjesec() {
	return mjesec;
}
public void setMjesec(int mjesec) {
	this.mjesec = mjesec;
}
public boolean isPlatio() {
	return platio;
}
public void setPlatio(boolean platio) {
	this.platio = platio;
}
public Placanje(int mjesec, boolean platio) {
	super();
	this.mjesec = mjesec;
	this.platio = platio;
}




}
